#operadores de associação
lista1 = [1, 2, 3, 4, 5]
print(3 in lista1)      #verifica se o valor 3 está na lista
print(6 not in lista1)  #verifica se o valor 6 não está na lista
print(10 in lista1)     #verifica se o valor 10 está na lista

