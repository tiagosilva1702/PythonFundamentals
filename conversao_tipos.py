# inteiro para float
preco = 10
print(preco)

preco = float(preco)
print(preco)    

preco = 10/2
print(preco)

#float para inteiro
quantidade = 3.5
print(quantidade)   

quantidade = int(quantidade)
print(quantidade)

#conversao por divisão
total = 10
por_pessoa = total / 3
print(por_pessoa)

por_pessoa = int(total / 3)
print(por_pessoa)  

#conversao por parte inteira
print(por_pessoa//3)

#conversao por arredondamento
por_pessoa = round(total / 3)
print(por_pessoa)

