#divisão
resultado = 10 / 3
print("Divisão (10 / 3):", resultado)

#dividão inteira
resultado = 10 // 3
print("Divisão Inteira (10 // 3):", resultado)

#módulo: resto da divisão
resultado = 10 % 3
print("Módulo (10 % 3):", resultado)

#exponenciação
resultado = 2 ** 3
print("Exponenciação (2 ** 3):", resultado)